package quoted_printable;

sub qprdecode {
    local($_) = shift;

    s/[^\S\r\n]*(\r?\n)/$1/g;	# remove trailing whitespace on each line
    s/\=\r?\n//g;		# remove soft linebreaks
    s/=([0-9A-F]{2})/pack("H2",$1)/ge;	# convert hex codes
    $_;				# return result
}

