int
main ()
{
  i18n_vars *vars;
  i18n_handle *i18n;
  int i = 0;

  vars = i18n_vars_new();
  i18n_vars_add(vars, "foo", "FOO!");

  i18n = i18n_new("test_i18n", "en");

  while (tags[i]) {
    char *got; 

    got = i18n_get(i18n, tags[i], NULL, vars);
    printf ("     tag: '%s'\n", tags[i]);
    printf ("     got: '%s'\n", got);

    i++;
  }

  i = 0;
  while (strings[i]) {
    char *got; 

    got = i18n_interpolate(i18n, strings[i], NULL);
    printf ("     str: '%s'\n", strings[i]);
    printf ("     got: '%s'\n", got);

    i++;
  }

  i18n_destroy (i18n);

  return 0;
}
